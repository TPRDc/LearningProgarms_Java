public class iii_Datatypes {
    public static void main(String[] args) {
// PERMEATIVE DATA TYPES 
        // Integers DataTypes
        byte aSingleByte = 100; // -128 to 127
        short aSmallNumber = 20000; // -32,768 to 32,767
        long aLagerNumber = 9223372036854775807L; // -9223372036854775808L to 9223372036854775807L 
        // by adding (L OR l) at the end of a number, this tell's the complier that the number is a long
        int anInteger = 2147483647; // -2147483648 to 2147483647

        // Decimal DataTypes
        double aDouble = 1.79769313; // 4.9E-324 to 1.7976931348623157E308
        float aFloat = 3.4028F; // 1.4E-45 TO 3.4028235E38F
        // by adding (F OR f) at the end of a number, this tell's the complier that the number is a float

        // Booleans DataTypes
        boolean isWeekend = false; boolean isWorkday = true;

        // Characters DataTypes
        char copyrightSymbol = '\u00A9';
        
        // Constants
        final int n=4;// data types beginning with the key word final, are data types which data cannot be change ones assign
        final String animal="hen";
        final double pie=22/7;
        final boolean fail=false;
        final char mark='F';
        final short t= 3000;
        final long q= 84839l;
        final float squr= 4.6f;
        final byte sql= 127;

// NON PERMEATIVE DATA TYPES
        // String DataTypes
        String name = "Obeten, Prince Philip";
    }
}